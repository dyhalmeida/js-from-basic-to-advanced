/**
 * Operadores Lógicos
 * && - And | Todas expressões devem ser true para a expressão completa ser true
 * || - Or | Apenas uma expressão deve ser true para a expressão completa ser true
 * ! - Not | Torna a expressão (true em false) ou (false em true)
 */
console.log(true && true); // true
console.log(true && false); // false
console.log(false && false); // false

console.log(true || true); // true
console.log(true || false); // true
console.log(false || false); // false

console.log(!true); // false
console.log(!false); // true
