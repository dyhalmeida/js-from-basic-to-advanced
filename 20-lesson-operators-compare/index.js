/**
 * Operadores de comparação
 * ------------------------
 * > maior que
 * >= maior que ou igual a
 * < menor que
 * <= menor que ou igual a
 * == igualdade
 * === igualdade estrita
 * != diferente
 * !== diferente estrito
 */
console.log(10 > 5); // true
console.log(10 >= 11); //false
console.log(10 < 11); // true
console.log(10 <= 11); // true
console.log(10 == "10"); // true
console.log(10 === "10"); // false
console.log(10 != "10"); // false
console.log(10 !== "10"); // true
